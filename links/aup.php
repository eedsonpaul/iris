        <td colspan="6" align="center"><b>APPROVED ACCEPTABLE USE POLICY FOR INFORMATION TECHNOLOGY (IT) RESOURCES OF THE UP SYSTEM</b><br>
          <br><br></td></tr>
     <tr valign="top"><td colspan="6"><b>Section 1. Policy Statement</b></td></tr>
     <tr valign="top">
      <td></td>
      <td>(a)</td>
      <td colspan="4">Computers and networks are powerful technologies for accessing and distributing information and knowledge. They are strategic technologies for the current and future needs of the UP SYSTEM.<td>
     </tr>
     <tr valign="top">
      <td></td>
      <td>(b)</td>
      <td colspan="4">For now, computing facilities and network infrastructure are a costly resource and thus must be used solely for teaching, learning, research, and other officially-sanctioned activities. Also, since these technologies allow individuals to access and copy information from remote sources, users must respect the rights of others, particularly to their privacy and intellectual property. There is therefore a need for rules and regulations to ensure equitable, secure and reliable access to these resources. The following regulations will govern the use of computing facilities, networks and other Information Technology (IT) resources of the University of the Philippines System.</td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(c)</td>
      <td colspan="4">These regulations aim to: </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">ensure an information infrastructure that promotes the basic missions of the UP SYSTEM in teaching, learning and research; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">protect the integrity, reliability, availability, confidentiality and efficiency of the IT resources of the UP SYSTEM; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(iii)</td>
      <td colspan="3">establish processes for addressing policy violations and providing sanctions for violators; </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(iv)</td>
      <td colspan="3">emphasize that the UP SYSTEM shall not be liable for any damages incurred from the use of IT resources and for any claims and suits arising from the unauthorized and irresponsible use of the same; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(v)</td>
      <td colspan="3">warn users that use of IT resources for partisan political activities as defined in relevant rules and regulations of the civil service commission or the university of the Philippines, or for any unauthorized commercial purposes is prohibited; and </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(vi)</td>
      <td colspan="3">notify users of the existence of this Policy. <td>
     </tr>
     <tr valign="top"><td colspan="6"><b>Section 2. Basic Standards</b></td></tr>
     <tr valign="top">
      <td></td>
      <td>(a)</td>
      <td colspan="4">The same standards and principles of intellectual and academic freedom developed for university libraries shall be applied to material received from the network. The same standards of intellectual and academic freedom developed for faculty and student publication in traditional media shall be applied to publication in computer media. <td>
     </tr>
     <tr valign="top">
      <td></td>
      <td>(b)</td>
      <td colspan="4"> As constituents of the academic community, faculty, students, and academic and non-academic staff should be free, individually and collectively, to express their views on issues of institutional policy and on matters of general interest to the academic body. The constituents of the academic community should have clearly defined means to participate in the formulation and application of institutional policy affecting academic and student affairs. The actions of the constituents of the academic community within the areas of its jurisdictions should be reviewed only through orderly prescribed procedures. </td>
     </tr>
     <tr valign="top"><td colspan="6"><b>Section 3. Definitions</b></td></tr>
     <tr valign="top">
      <td></td>
      <td>(a)</td>
      <td colspan="4"><i>Agreement Form</i> means document in which the user undertakes to comply with this Policy. The form may be electronic. </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td>(b)</td>
      <td colspan="4"><i>Confidential information</i> means data or information which on its face is not intended for unrestricted dissemination. Examples include student records, examination archives, proprietary technical information, disciplinary case records, administrative records, and the like. <td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(c)</td>
      <td colspan="4"><i>Document</i> when used in this Policy shall refer both to the paper and its electronic format. <td>
     </tr>
     <tr valign="top">
      <td></td>
      <td>(d)</td>
      <td colspan="4"><i>Information Technology System</i> or <i>IT System</i> includes computers, terminals, printers, networks, modem banks, online and offline storage media and related equipment, and software, databases and other data files that are owned, managed, or maintained by any unit of the University of the Philippines. <br>For purposes of this Policy, any other equipment, computer unit or external network, when attached to, or used to access and/or interact with any component of, the IT System may also be considered part of the IT System. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(e)</td>
      <td colspan="4"><i>Private files</i> means information that a user would reasonably regard as private. Examples include the contents of electronic mail boxes, private file storage areas of individual users, and information stored in other areas that are not public, even if no measure has been taken to protect such information. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(f)</td>
      <td colspan="4"><i>System and Network Administrator</i> means a person designated to manage the particular system assigned to her/him, to oversee the day-to-day operation of the system, or to preliminarily determine who is permitted access to particular facilities and resources of the IT System, whether hired on a temporary, contractual or permanent basis. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(g)</td>
      <td colspan="4"><i>UP SYSTEM</i> means the University of the Philippines System and all its constituent units. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(h)</td>
      <td colspan="4"><i>User</i> means any person, whether authorized or not, who makes any use of the IT System or any of its components by any means or from any location. </td>
     </tr> 
     <tr valign="top"><td colspan="6"><b>Section 4. Scope And Applicability</b></td></tr>
     <tr valign="top">
      <td></td>
      <td>(a)</td>
      <td colspan="4"><i>General Coverage.</i></td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">This Policy applies to all facilities within the IT System and all its users. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">All users should be aware of these regulations, and should realize that when using the computers within the UP SYSTEM, they are bound by these regulations. Users may be required to sign a form agreeing to comply with this Policy. However, failure to sign the agreement form will not release users from coverage of this Policy. </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td>(b)</td>
      <td colspan="4"><i>Local and External Conditions of Use.</i></td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">Individual units within the UP SYSTEM may define additional "conditions of use" for components of the ITSystem under their control. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">These conditions must be consistent with this overall policy but may provide additional detail, guidelines, restrictions, and/or enforcement mechanisms. These units will be responsible for publishing the regulations they establish and their policies concerning the authorized and appropriate use of the equipment for which they are responsible. Copies of these policies should be given to the President, Vice-President for Development, the Intellectual Property Office and the Office of Legal Services. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(iii)</td>
      <td colspan="3">Where use of external networks is involved, policies governing such use will be applicable and must be adhered to. </td>
     </tr>
     <tr valign="top"><td colspan="6"><b>Section 5. General Responsibilities</b></td></tr>
     <tr valign="top">
      <td></td>
      <td>(a)</td>
      <td colspan="4"><i>General Responsibilities of Users.</i> In general, users of the IT System must: </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">use the IT System only for its intended purpose, and refrain from misusing or abusing it; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">maintain the integrity, reliability, availability, confidentiality and efficiency of computer-based information resources; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(iii)</td>
      <td colspan="3">refrain from seeking to gain unauthorized access or exceed authorized access; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(iv)</td>
      <td colspan="3">respect software copyright and licenses and other intellectual property rights; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(v)</td>
      <td colspan="3">respect the rights of other computer users; and </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(vi)</td>
      <td colspan="3">be aware that although computing and information technology providers throughout the university are charged with preserving the integrity and security of resources, security sometimes can be breached through actions beyond their control. Users are therefore urged to take appropriate precautions such as safeguarding their account and password, taking full advantage of file security mechanisms, backing up critical data and promptly reporting any misuse or violations of the policy.<br> Every member of the university community has an obligation to report suspected violations of the Acceptable Use Policy for Information Technology of the U.P. System or any of its units. Reports should be directed to the system and network administrators, Chairs, Deans, Chancellors or the President. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(b)</td>
      <td colspan="4"><i>General Responsibilities Of System And Network Administrators</i></td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">System and network administrators and providers of university Information Technology resources have the additional responsibility of ensuring the integrity, confidentiality, and availability of the resources they are managing. Persons in these positions are granted significant trust to use their privileges appropriately for their intended purpose and only when required to maintain the system. Any private information seen in carrying out these duties must be treated in the strictest confidence, unless it relates to a violation or the security of the system. </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">System and network administrators are expected to treat the contents of electronic files as private and confidential. Any inspection of electronic files, and any action based upon such inspection, will be governed by this Policy, other university rules and all applicable laws. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(c)</td>
      <td colspan="4"><i>General Responsibilities Of University Administrators</i></td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3">To be informed and knowledgeable about these policies </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="3">To initiate systematic programs to inform academic and non-academic personnel of these policies </td>
     </tr>
     <tr valign="top"><td colspan="6"><b><a name="6">Section 6. Appropriate Use</a></b></td></tr>
     <tr valign="top">
      <td></td>
      <td><a name="6a">(a)</a></td>
      <td colspan="4"><i>Appropriate Use.</i> Users may only use the IT System for its authorized purposes, which is to support the research, education, clinical, administrative and other functions of the UP SYSTEM. The particular purposes of any of the components of the IT System, as well as the nature and scope of authorized incidental personal use, may vary according to the duties and responsibilities of a user. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td><a name="6b">(b)</a></td>
      <td colspan="4"><i>Proper Authorization.</i> Users may access only those facilities and components of the IT System that are consistent with their authorization coming from competent authorities. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td>(c)</td>
      <td colspan="4"><i>Specific Proscriptions on Use.</i> The following categories of use of the IT System are considered prohibited and/or inappropriate: </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(i)</td>
      <td colspan="3"><b>Uses Contrary To Law</b></td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ci1">1.</a></td>
      <td colspan="2"><i>Unlawful use.</i> Users may not use the IT System for any activity that is contrary to any law or administrative rule or regulation, or to encourage any such unlawful activity. Violators shall suffer a penalty ranging from suspension for one year to expulsion or dismissal </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ci2">2.</a></td>
      <td colspan="2"><i>Infringement of protected material.</i> Users must not infringe on the copyright and other property rights covering software, databases and all other copyrighted material such as text, images, icons, retrieved from or through the IT System. These acts shall include, but is not limited to, the unauthorized copying, reproduction, dissemination, distribution, importation, use, removal, alteration, substitution, modification, storage, unloading, downloading, communication, publication or broadcasting of such material. Users must properly attribute any material they copy from or through the IT System. Users are reminded that the infringement of intellectual property rights belonging to others through the use of telecommunications networks is a criminal offense under Section 33(b) of the Electronic Commerce Act. Violators shall suffer a penalty ranging from suspension for one month to expulsion or dismissal. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ci3">3.</a></td>
      <td colspan="2"><i>Hacking.</i> Users may not use the IT System to gain unauthorized access into or interfere with another computer, system, server, information or communication system, or to obtain any access in order to corrupt, alter, steal or destroy any such system or information within such system or to introduce viruses. Users are reminded that all of the foregoing acts constitute the crime of Hacking under Section 33(a) of the Electronic Commerce Act and are punishable by mandatory imprisonment and/or a fine. Violators shall suffer a penalty ranging from suspension for one year to expulsion or dismissal. The penalty shall carry with it permanent withdrawal of all IT privileges.</td>
     </tr>  
     <tr valign="top">
      <td></td>
      <td></td>
      <td>(ii)</td>
      <td colspan="4"><b>Uses Inconsistent With The Purposes Of The UP System</b></td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii1">1.</a></td>
      <td colspan="2"><i>Cheating.</i> Users may not use the IT System to engage in cheating or academic dishonesty. Acts prohibited under this provision include but are not limited to the following: </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>a.</td>
      <td>Copying a computer file that contains another person's work and submitting it for one's own credit; </td>
     </tr>
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>b.</td>
      <td>Copying a computer file that contains another person's work and using it as a model for one's own work; </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>c.</td>
      <td>Collaborating on a work, sharing the computer files and submitting the shared file, ora modification thereof, as one's individual work, when the work is supposed to be done individually; and </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>d.</td>
      <td>Communicating with another person on-line during the conduct of an examination. Violators shall suffer a penalty of suspension for not less than one semester. Students found guilty of cheating shall be barred form graduating with honors, even if their weighted average is within the requirement for graduation with honors. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii2">2.</a></td>
      <td colspan="2"><i>Political use.</i> Users may not use the IT System for any partisan political activities. Violators shall suffer a penalty ranging from suspension for one month to one year. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td>3.</td>
      <td colspan="2"><i>Unauthorized Commercial use.</i></td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii3a">a.</a></td>
      <td>Users may not use the IT System for commercial purposes, except as permitted under other written policies of the UP SYSTEM or with the written approval of a competent authority. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>b.</td>
      <td>Violators shall suffer a penalty ranging from suspension for one month to one year with fine. If the violator is a student, the fine shall be P1,000.00 or the amount equivalent to the earnings, whichever is higher. If the violator is a faculty member or an employee, the fine shall be one-half of his monthly salary or the amount equivalent to the earnings, whichever is higher. </td>
     </tr> 
     <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii4">4.</a></td>
      <td colspan="2"><i>Personal use.</i> Users may not use the IT System for personal activities not related to appropriate University functions except in a purely incidental manner. Violators shall suffer a penalty ranging from suspension for one month to one year. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii5">5.</a></td>
      <td colspan="2"><i>Unauthorized gaming or entertainment.</i> Users may not play games or use entertainment software on or through the IT System unless authorized in writing by competent authorities. Violators shall suffer a penalty ranging from suspension for one week to one year; provided, that the penalty for habitual offense shall be expulsion or dismissal. The presence of game software or any part thereof may be presumptive evidence of unauthorized gaming or entertainment. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6cii6">6.</a></td>
      <td colspan="2"><i>Use contrary to University policy or contract.</i> Users may not use the IT System in violation of other policies of the University, or in any manner inconsistent with the contractual obligations of the University. Violators shall suffer a penalty ranging from suspension for one week to one year in addition to the penalty of the offense facilitated through IT network. </td>
     </tr> 
    <tr valign="top">
      <td></td> 
      <td></td>
      <td>(iii)</td>
      <td colspan="4"><b>Uses That Damage The Integrity, Reliability, Confidentiality And Efficiency Of The IT System</b></td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td>1.</td>
      <td colspan="2"><i>Software and hardware installation and removal.</i> Unless properly authorized, users may not destroy, remove, modify or install any computer equipment, peripheral, operating system, disk partition, software, database, or other component of the IT System; or connect any computer unit or external network to the IT System. Violators shall suffer a penalty ranging from suspension for one month to expulsion.</td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td>2.</td>
      <td colspan="2"><i>Unauthorized or destructive programs.</i> Unless properly authorized and part of her/his administrative or academic duties, users may not develop or use programs on the IT System that may or are intended to: </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>a.</td>
      <td>interfere with the ability of the UP SYSTEM to enforce these policies; </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>b.</td>
      <td>damage any software or hardware component of the system; </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>c.</td>
      <td>modify normally protected or restricted portions of the system or user accounts; </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>d.</td>
      <td>access private or restricted portions of the system; or </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>e.</td>
      <td>interfere with or disrupt other computer users. Violators shall suffer a penalty ranging from suspension for one year to expulsion. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ciii3">3.</a></td>
      <td colspan="2"><i>Destructive acts.</i> Users may not attempt to crash, tie up, or deny any service on, the IT System. Violators shall suffer a penalty ranging from suspension for one year to expulsion. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ciii4">4.</a></td>
      <td colspan="2"><i>Unauthorized access.</i> Users may not attempt to gain unauthorized access, exceed authorized access, or enable unauthorized access to the IT System, or to other networks or systems of which the IT System is a part. Violators shall suffer a penalty ranging from suspension for one month to one year. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td>5.</td>
      <td colspan="2"><i>Password protection.</i> A user who has been authorized to use a password-protected account may not disclose such password or otherwise makes the account available to others without permission of the system administrator. Violators shall suffer a penalty ranging from suspension for one week to one year. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td>6.</td>
      <td colspan="2"><i>Concealing access.</i> Users may not conceal, delete, or modify information or records pertaining to access to the IT System at the time of access, or alter system logs after such access for the purpose of concealing identity or to hide unauthorized use. Users may not conceal their own identity or masquerade as other users when accessing, sending, receiving, processing or storing through or on the IT System. Violators shall suffer a penalty ranging from suspension for one year to expulsion. </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td><a name="6ciii7">7.</a></td>
      <td colspan="2"><i>Prohibited material.</i> Users may not publish (on mailing lists, bulletin boards, and the World Wide Web) or disseminate prohibited materials over, or store such information on, the IT System. Prohibited materials under this provision include but are not limited to the following: </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>a.</td>
      <td>Any collection of passwords, personal identification numbers (PINs), private digital certificates, credit card numbers, or other secure identification information; </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>b.</td>
      <td>Any material that enables others to gain unauthorized access to a computer system. This may include instructions for gaining such access, computer code, or other devices. This would effectively preclude displaying items such as 'Hackers Guides', etc.; </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
      <td></td>
      <td>c.</td>
      <td>Any material that permits an unauthorized user, who has gained access to a system, to carry out any modification of the computer programs or data stored in the system; and </td>
     </tr> 
    <tr valign="top">
      <td></td>
      <td></td>
      <td></td>
